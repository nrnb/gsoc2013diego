    public int ChiamataServer(CyNetwork network,String strUser,long lngCent){
    if (network.getNodeCount() == 0) {
                    System.out.println("Network empty");
                    JOptionPane.showMessageDialog(null, "Network empty", "CentiScaPe", JOptionPane.WARNING_MESSAGE);
                    return 0;
    }  
    String strNomeRete = "" + network.getSUID(); //ID RETE
    String strRete = strNomeRete.replaceAll(" ", "_");
    CentiScaPeClient centiscapeclient = new CentiScaPeClient(network);
        try {
            long lngID = centiscapeclient.lngUseCentiscapeWS(strUser,lngCent, strRete, network, this);
            if (lngID==-2){
                System.out.println("ERROR network="+strNomeRete);
                return 0;                                        
            }
            if (lngID >= 0) {
                String strFileName = String.format("%s_(%d).xgmml", strRete, lngID);
                JOptionPane.showMessageDialog(null, "Results (in file: " + strFileName + ") will be sent to your mail as soon as they are ready", "CentiScaPe", JOptionPane.INFORMATION_MESSAGE);
            } else 
                if (lngID == -1) {                            
                    System.out.println("ERROR network="+strNomeRete);
                    JOptionPane.showMessageDialog(null, "Call server failed", "CentiScaPe", JOptionPane.INFORMATION_MESSAGE); 
                    return 0;
                }
        } catch (IOException e) {
            System.err.println("Couldn't get I/O for the connection to:");
            return 0;
        }
        return 1;
    }
